async function translateNotExisting(obj, baseText, yandex) {
    let t = obj['en'];
    if (!t) {
        t = baseText;
    }

    if (t) {
        for (let l in languages) {
            if (!obj[l]) {                
                const time = new Date().getTime();
                obj[l] = await translate(t, l, yandex);
                console.log('en -> ' + l + ' ' + (new Date().getTime() - time) + ' ms');
            }
        }
    }
}

